public class Datatype{
    public static void main(String[] args) {
        System.out.println("First Datatype program in java");
        byte a=18;
        int b=1000;
        short c=23421;
        long d=1023000L;
        float e=10.00000f;
        boolean f= true;
        System.out.println(a*b*c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
